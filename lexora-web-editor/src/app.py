# =============================================================================
# HELPER CLASS: OutputCapture - Console Output Interceptor
# =============================================================================
class OutputCapture:
    """
    Captures stdout from print() statements during code execution.
    
    This class replaces sys.stdout temporarily to capture all output
    generated by Display commands and other print operations.
    
    Attributes:
        output (list): List of captured output strings
    
    Example:
        >>> capture = OutputCapture()
        >>> sys.stdout = capture
        >>> print("Hello")
        >>> sys.stdout = sys.__stdout__
        >>> print(''.join(capture.output))  # Outputs: Hello
    """
    def __init__(self):
        self.output = []

    def write(self, text):
        self.output.append(text)

    def flush(self):
        pass

