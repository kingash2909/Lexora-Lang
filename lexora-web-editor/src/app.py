@app.route('/execute', methods=['POST'])
def execute():
    """Execute Lexora code and return the output"""
    code = request.json.get('code', '')
    
    # Create a fresh interpreter for each execution to avoid state pollution
    current_interpreter = SimpleEnglishInterpreter()
    
    # Capture stdout to get the output
    stdout = sys.stdout
    output_capture = OutputCapture()
    sys.stdout = output_capture
    
    try:
        # Execute the code as a script
        lines = code.split('\n')
        current_interpreter.execute_script(lines)
        
        # Restore stdout and return results
        sys.stdout = stdout
        output_str = ''.join(output_capture.output)
        return jsonify({
            'status': 'success',
            'output': output_str if output_str else "Program finished with no output."
        })
    
    except Exception as e:
        sys.stdout = stdout
        return jsonify({
            'status': 'error',
            'error': str(e)
        })

